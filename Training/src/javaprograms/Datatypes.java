package javaprograms;

public class Datatypes {

	public static void main(String[] args) {
		
		int i=5;
		char c='a';
		boolean b=true;
		float f= 9.14f;
		double d= 15.876;
		
		System.out.println("get int value:"+i);
		System.out.println("get char value:"+c);
		System.out.println("get boolean value:"+b);
		System.out.println("get float value:"+f);
		System.out.println("get double value:"+d);
		
	}
}
