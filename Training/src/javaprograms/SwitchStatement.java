package javaprograms;

public class SwitchStatement {

	public static void main(String[] args) {

		String name = "monica";

		switch(name)
		{
		case("bhdai"):
			System.out.println("the case is a");
		break;

		case("monica"):
			System.out.println("the case is b");
		break;

		default:
			System.out.println("both are wrong");
			break;
		}




	}

}
